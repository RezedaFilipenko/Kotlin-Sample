package mathematics

class Mathematics {
    var number1 = 0
    var number2 = 0

    fun sum(number1: Int, number2:Int){
        var c = number1 + number2
        println(c)
    }

    fun subtraction(number1: Int, number2: Int){
        var c = number1-number2
        println(c)
    }

    fun multiplication(number1: Int, number2: Int){
        var c = number1*number2
        println(c)
    }

    fun division(number1: Int, number2: Int){
        var c = number1/number2
        println(c)
    }
}